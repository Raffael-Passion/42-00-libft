#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		n;
	int		res;

	i = 0;
	n = 1;
	res = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = ((str[i] - '0') + (res * 10));
		i++;
	}
	return (res * n);
}
